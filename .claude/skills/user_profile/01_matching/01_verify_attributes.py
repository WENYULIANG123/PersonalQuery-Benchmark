#!/usr/bin/env python3
"""
Execute Match Tasks Script
Part 2 of the Matching Workflow: LLM Execution

This script reads the tasks generated by `generate_match_tasks.py` and executes the LLM calls.
It focuses on:
1. Reading task data (Step 1 input, Step 2 input, Step 3 metadata)
2. Constructing Prompts dynamically
3. Calling LLM for each step sequentially
4. Saving final results

Input: match_tasks_[USER_ID].json
Output: match_results_[USER_ID].json
"""
import os
import sys
import json
import argparse
import re
from datetime import datetime
from typing import Dict, List, Any
import concurrent.futures

# Add parent directory for llm_client import
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)) + "/../../")
from llm_client import LLMClient

def log_with_timestamp(message: str):
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    print(f"[{timestamp}] {message}", flush=True)

def parse_response(response: str) -> Dict:
    """Clean and parse JSON from LLM response"""
    try:
        if not response:
            return None
            
        json_content = response
        if "```json" in response:
            match = re.search(r'```json\s*(.*?)\s*```', response, re.DOTALL)
            if match:
                json_content = match.group(1)
        elif "```" in response:
            match = re.search(r'```\s*(.*?)\s*```', response, re.DOTALL)
            if match:
                json_content = match.group(1)
        else:
            match = re.search(r'\{.*\}', response, re.DOTALL)
            if match:
                json_content = match.group(0)
                
        return json.loads(json_content)
    except Exception as e:
        # log_with_timestamp(f"Error parsing JSON: {e}")
        return None

def call_llm(prompt: str) -> str:
    try:
        client = LLMClient()
        response = client.call(prompt, max_tokens=1024)
        return response
    except Exception as e:
        log_with_timestamp(f"Error calling LLM: {e}")
        return None

# --- Prompt Builders ---

def build_step1_prompt(data: Dict) -> str:
    asin = data.get('asin')
    title = data.get('title')
    user_prefs = data.get('user_prefs_candidates', [])
    
    return f"""**User Preference Analysis Task**

You are analyzing a user's expressed preferences for a product. Extract ONLY attributes they mentioned with positive or neutral sentiment.

**Product ASIN**: {asin}
**Product Title**: {title}

**User's Expressed Preferences**:
The user reviewed this product and shared the following feedback:

{json.dumps(user_prefs, indent=2, ensure_ascii=False)}

---

**Your Task**:
Analyze the user's preferences and extract key attributes/dimensions they care about.

**Rules**:
1. Only include attributes the user mentioned with POSITIVE or NEUTRAL sentiment
2. Group related attributes together (e.g., "easy to use" + "simple instructions" = "Ease of Use")
3. Preserve user's original wording when possible
4. Output 5-10 key attributes maximum

**Output Format** (JSON):
```json
{{
  "user_preferences": [
    {{"attribute": "Ease of Use", "evidence": "User said: 'This is the coolest punch!'", "dimension": "ease_of_use"}},
    {{"attribute": "High Banner Yield", "evidence": "User said: 'I was able to cut 8 banners...'", "dimension": "efficiency"}}
  ]
}}
```

Analyze the preferences now."""

def build_final_match_prompt(step1_res: Dict, step3_data: Dict) -> str:
    asin = step3_data.get('asin')
    title = step3_data.get('title')
    brand = step3_data.get('brand')
    
    # Extract all metadata fields
    # Handle both 'feature' (correct) and legacy 'features' just in case
    features = step3_data.get('feature') or step3_data.get('features', [])
    if isinstance(features, str):
        features = [features]
    display_features = features[:8] if features else [] # Increase to 8 bullets
    
    description = step3_data.get('description', [])
    if isinstance(description, str):
        description = [description]
    display_description = '\n'.join(description[:3]) if description else "N/A" # First 3 paragraphs
    
    category = step3_data.get('category', [])
    display_category = ' > '.join(category) if category else "N/A"
    
    price = step3_data.get('price', '')
    
    details = step3_data.get('details', {})
    display_details = '\n'.join([f"- {k}: {v}" for k, v in details.items()]) if details else "N/A"
    
    tech1 = step3_data.get('tech1', '')
    tech2 = step3_data.get('tech2', '')
    fit = step3_data.get('fit', '')
    
    extra_specs = []
    if tech1: extra_specs.append(f"Tech1: {tech1}")
    if tech2: extra_specs.append(f"Tech2: {tech2}")
    if fit: extra_specs.append(f"Fit: {fit}")
    display_specs = '\n'.join(extra_specs) if extra_specs else "N/A"
    
    user_prefs = step1_res.get('user_preferences', [])
    
    return f"""**Final Attribute Matching Task**

**Product**: {asin}

---

## 1. User Preferences (Source: Direct Review)

The user reviewed THIS product and expressed:

{json.dumps(user_prefs, indent=2, ensure_ascii=False)}

---

## 2. Product Metadata (Full Context)

**Basic Info**:
*   **Title**: {title}
*   **Brand**: {brand}
*   **Category**: {display_category}
*   **Price**: {price or 'N/A'}

**Features (Bullet Points)**:
{' | '.join(display_features) if display_features else 'N/A'}

**Description**:
{display_description}

**Product Details**:
{display_details}

**Technical Specs**:
{display_specs}

---

## Your Mission

Select **most representative attributes** for this product based on:

1. **User Preferences** - What the user explicitly said about THIS product
2. **Metadata Validation** - What does the product metadata EXPLICITLY support?

**CRITICAL RULES**:

1. **VALIDATION REQUIRED** - Each attribute MUST be supported by BOTH User Preferences AND Product Metadata (Any field above).
2. **STRICT EVIDENCE TRACKING**:
   - `metadata_evidence`: Quote from product metadata (Title, Feature, Description, etc.)
   - `preference_evidence`: Quote from user's own review
3. **NO QUOTA** - Select 0, 1, 2, 3, or more attributes depending on validation

**Output Format** (JSON):

```json
{{
  "selected_attributes": [
    {{
      "attribute": "Exact attribute name",
      "dimension": "feature|brand|design|etc",
      "reasoning": "Brief explanation of why selected",
      "source": "direct_preference",
      "metadata_evidence": "Quote from product metadata",
      "preference_evidence": "Quote from user review"
    }}
  ],
  "summary_reasoning": "Overall explanation",
  "validation_passed": true
}}
```

Select attributes now."""

# --- Execution Logic ---

def execute_task(task: Dict) -> Dict:
    asin = task.get('asin')
    step3_input = task.get('step3_input', {})

    # Extract category from step3_input (metadata)
    category_list = step3_input.get('category', [])
    if isinstance(category_list, list) and category_list:
        category = category_list[-1] if len(category_list) > 0 else 'Unknown'
    else:
        category = step3_input.get('category', 'Unknown')
        if isinstance(category, list):
            category = category[-1] if category else 'Unknown'

    result = {
        'asin': asin,
        'category': category,
        'steps': {},
        'final_match': None,
        'status': 'pending'
    }

    # --- Step 1 ---
    step1_input = task.get('step1_input', {})
    if not step1_input.get('user_prefs_candidates'):
        result['status'] = 'skip_no_prefs'
        return result

    prompt1 = build_step1_prompt(step1_input)
    resp1 = call_llm(prompt1)
    step1_res = parse_response(resp1)

    if not step1_res:
        result['status'] = 'step1_failed'
        return result

    result['steps']['step1'] = step1_res

    # --- Final Match (formerly Step 3) ---
    prompt_final = build_final_match_prompt(step1_res, step3_input)
    resp_final = call_llm(prompt_final)
    step_final_res = parse_response(resp_final)

    if step_final_res:
        result['final_match'] = step_final_res
        result['status'] = 'success'
        result['selected_attributes'] = step_final_res.get('selected_attributes', [])
        result['summary_reasoning'] = step_final_res.get('summary_reasoning', '')
    else:
        result['status'] = 'final_match_failed'

    return result

def main():
    parser = argparse.ArgumentParser(description="Execute Match Tasks (LLM Execution)")
    parser.add_argument("--task-file", required=True, help="Path to match_tasks_[USER_ID].json")
    parser.add_argument("--output-dir", default="/home/wlia0047/ar57/wenyu/result/user_profile/preference_match_results")
    parser.add_argument("--max-workers", type=int, default=3)
    args = parser.parse_args()
    
    os.makedirs(args.output_dir, exist_ok=True)
    
    log_with_timestamp(f"Loading tasks from {args.task_file}")
    with open(args.task_file, 'r', encoding='utf-8') as f:
        task_data = json.load(f)
        
    user_id = task_data.get('user_id')
    tasks = task_data.get('tasks', [])
    
    results = []
    
    log_with_timestamp(f"Executing {len(tasks)} tasks with {args.max_workers} workers...")
    
    with concurrent.futures.ThreadPoolExecutor(max_workers=args.max_workers) as executor:
        future_to_task = {executor.submit(execute_task, task): task for task in tasks}
        
        completed = 0
        for future in concurrent.futures.as_completed(future_to_task):
            task = future_to_task[future]
            try:
                res = future.result()
                results.append(res)
                completed += 1
                if completed % 5 == 0:
                    log_with_timestamp(f"Progress: {completed}/{len(tasks)} tasks executed.")
            except Exception as e:
                log_with_timestamp(f"Error executing task for {task.get('asin')}: {e}")
                
    output_data = {
        'user_id': user_id,
        'timestamp': datetime.now().isoformat(),
        'total_results': len(results),
        'results': results
    }
    
    output_file = os.path.join(args.output_dir, f"match_{user_id}.json")
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(output_data, f, indent=2, ensure_ascii=False)
        
    log_with_timestamp(f"DONE! Saved results to {output_file}")

if __name__ == "__main__":
    main()
